def add(a,b):
    """
    Additionne deux nombres.

    Args:
        a (float): Le premier nombre.
        b (float): Le deuxième nombre.

    Returns:
        float: La somme de a et b.
    """
    return a + b

def subtract(a,b):
    """
    Soustrait deux nombres.

    Args:
        a (float): Le premier nombre.
        b (float): Le deuxième nombre.

    Returns:
        float: La différence.
    """
    return a - b

def multiply(a,b):
    """
    Multiplie deux nombres.

    Args:
        a (float): Le premier nombre.
        b (float): Le deuxième nombre.

    Returns:
        float: Le produit.
    """
    return a * b

def divide(a,b):
    """
    Divise deux nombres.

    Args:
        a (float): Le numérateur.
        b (float): Le dénominateur.

    Returns:
        float: Le quotient.
    """
    return a // b
